#include "qlist.h"
#include "qcodemapper.h"

static QList<QCodeMapper> all;

QCodeMapper::QCodeMapper()
{
    all.append(this);
}

QCodeMapper::~QCodeMapper()
{
    all.remove(this);
}

QCodeMapper* QCodeMapper::mapperFor(const char* locale)
{
    QListIterator<QCodeMapper> i(all);
    QCodeMapper* result;
    while ( (result=i) ) {
	if ( result->canConvert(locale) )
	    break;
	++i;
    }
    return result;
}


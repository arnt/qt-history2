#include "qbitmap.h"

#if defined(DEBUG)
static char ident[] = "$Id: //depot/qt/main/src/kernel/qbitmap.cpp#4 $";
#endif


/*!
\class QBitmap qbitmap.h

\brief The QBitmap class provides monochrome (1 bit depth) pixmaps.
*/

/*!
\fn QBitmap::QBitmap()
Constructs a null bitmap.
*/

/*!
\fn QBitmap::QBitmap( int w, int h )
Constructs an uninitialized \e w x \e h bitmap.
*/

/*!
\fn QBitmap::QBitmap( int w, int h, const char *bits, bool isXbitmap=FALSE )
Constructs a \e w x \e h bitmap and sets the contents to \e bits.

The \e isXbitmap should be TRUE if \e bits was generated by the
X-Windows bitmap program.  The X bitmap bit order is little endian.
The QImage documentation discusses bit order for monochrome images.
*/

/*!
\fn QBitmap::QBitmap( const QBitmap &bm )
Constructs a bitmap which is a copy of \e bm. 
*/

/*!
\fn QBitmap::QBitmap( const QImage &im )
Constructs a bitmap from the image description \e im.
*/

/*!
\fn QBitmap::&operator=( const QBitmap &bm )
Assigns the bitmap \e bm to this bitmap.
*/

/*!
\fn QBitmap::&operator=( const QImage &im )
Assigns the image description \e im to this bitmap.
*/


/*!
Returns TRUE to indicate that this pixmap object really is a bitmap.
Used internally.
*/

bool QBitmap::isBitmap() const
{
    return TRUE;
}

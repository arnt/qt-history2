CharacterView::CharacterView(QWidget *parent)
    : QWidgetView(parent)
{
    dragging = false;
    dragCursor = QCursor(QBitmap(32, 32, cursorImage, true),
                         QBitmap( 32, 32, cursorMask, true), -1, -1 );
    setMouseTracking(true);
}

void CharacterView::mousePressEvent(QMouseEvent *event)
{
    QPoint position;

    switch (event->button()) {
        case Qt::MidButton:
            dragPosition = event->pos();
            dragging = true;
            widget()->setCursor(dragCursor);
            break;
        default:
            QWidgetView::mousePressEvent(event);
    }
}

void CharacterView::mouseMoveEvent(QMouseEvent *event)
{
    if (dragging) {
        QPoint position = event->pos();
        int dx = position.x() - dragPosition.x();
        int dy = position.y() - dragPosition.y();
        horizontalScrollBar()->setValue(horizontalScrollBar()->value() - dx);
        verticalScrollBar()->setValue(verticalScrollBar()->value() - dy);
        dragPosition = position;
    }
    else {
        static_cast<CharacterWidget *>(widget())->showToolTip(event->globalPos());
        QWidgetView::mouseMoveEvent(event);
    }
}

void CharacterView::mouseReleaseEvent(QMouseEvent *event)
{
    if (dragging) {
        dragging = false;
        widget()->unsetCursor();
    }
    else
        QWidgetView::mouseReleaseEvent(event);
}

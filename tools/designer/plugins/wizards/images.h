{
    static QDict<QByteArray> dict;
    QByteArray* ba = dict.find(name);
    if ( !ba ) {
        for (int i=0; embed_vec[i].data; i++) {
    	if ( 0==strcmp(embed_vec[i].name, name) ) {
    	    ba = new QByteArray;
    	    ba->setRawData( (char*)embed_vec[i].data,
    			    embed_vec[i].size );
    	    break;
    	}
        }
        if ( !ba ) {
            static QByteArray dummy;
            return dummy;
        }
    }
    return *ba;
}

